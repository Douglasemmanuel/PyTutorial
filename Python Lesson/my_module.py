"""This module is for arithemetical operation"""

def Addition(a:int|float,b:int|float)-> int|float:
    """This Function Adds two numbers either float or int e.g add(1,2)->3"""
    Addition = a + b
    return Addition

def Substraction(a:int|float,b:int|float)->int|float:
    """This Function Substracts two numbers either float or int e.g add(2,1)->1"""
    Substraction = a - b
    return Substraction

def Division(a:int|float,b:int|float)->int|float:
    """This Function Divides two numbers either float or int e.g add(2,1)->1"""
    Division = a / b
    return Division

def multipication(a:int|float,b:int|float)->int|float:
    """This Function Multiplies two numbers either float or int e.g add(2,1)->2"""
    multiplication = a * b
    return multiplication


